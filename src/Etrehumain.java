public class Etrehumain {
    private String nom;
    private String prenom;
    private int age;


    public void setNom(String var) {
        nom = var;
    }
    public String getNom(){
        return nom;
    }

    public void setPrenom(String var) {
        prenom = var;
    }
    public String getPrenom(){
        return prenom;
    }

    public void setAge(int var) {
        age = var;
    }
    public int getAge(){
        return age;
    }

}
